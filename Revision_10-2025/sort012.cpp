#include <vector>
#include <iostream>
using namespace std;

class Solution{
  public:
  void sortColors(vector<int>& nums){
    int low = 0;
    int mid = 0;
    int high = nums.size()-1;
    while(mid <= high){
      if(nums[mid] == 0) {
        swap(nums[low], nums[mid]);
        low++;
        mid++;
      }
      else if(nums[mid] == 1) mid++;

      else{
        swap(nums[mid], nums[high]);
        high--;
      }
    }
    for(auto it: nums){
      cout<<it<<" ";
    }
    cout<<endl;
  }  
};


int main(){
    Solution sol;
    vector<int> nums = {2,0,1};
    // vector<int> nums = {2,0,2,1,1,0};
    sol.sortColors(nums);
    return 0;
}